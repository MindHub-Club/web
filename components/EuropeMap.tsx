export function EuropeMap({
  locations = [],
  selectedLocation = null,
  onLocationSelect,
  onLocationHover,
  className = '',
  showPotentialLocations = true,
  customStyles = {}
}: EuropeMapProps) {
  const { t } = useI18n();

  const {
    activeLocationColor = '#388cf3',
    potentialLocationColor = '#bcdaff',
    inactiveLocationColor = '#94a3b8',
    hoverColor = 'hsl(var(--secondary))'
  } = customStyles;

  const activeLocations = locations.filter(loc => loc.status === 'active');
  const potentialLocations = showPotentialLocations 
    ? locations.filter(loc => loc.status === 'potential')
    : [];

  const handleLocationClick = (locationId: string) => {
    onLocationSelect?.(locationId);
  };

  const handleLocationHover = (locationId: string | null) => {
    onLocationHover?.(locationId);
  };

  return (
    <div className={`relative ${className}`}>
      <svg
        viewBox="0 0 396 285"
        className="w-full h-auto p-8"
        style={{ maxWidth: "800px" }}
      >
        <defs>
          <style>
            {`
              .location-dot {
                cursor: pointer;
                transition: all 0.3s ease;
              }
              .location-dot:hover {
                transform: scale(1);
              }
              .location-text {
                transition: all 0.3s ease;
                pointer-events: none;
                opacity: 0;
              }
              .location-dot:hover + .location-text {
                opacity: 1;
              }
              .active-location {
                fill: ${activeLocationColor} !important;
              }
              .potential-location {
                fill: ${potentialLocationColor};
              }
              .inactive-location {
                fill: ${inactiveLocationColor};
              }
              .location-dot:hover {
                fill: ${hoverColor} !important;
              }
              .map-dot {
                fill: #202020;
              }
            `}
          </style>
        </defs>

        {/* Base map pattern - all the dot paths from the original SVG */}
        <g className="map-pattern">
          {DOT_PATTERN.map((path, index) => (
            <path key={index} d={path} className="map-dot" />
          ))}
        </g>

        {/* Active Locations */}
        {activeLocations.map((location) => (
          <g key={location.id} className="location-group">
            <circle
                      cx={location.coordinates.x}
                      cy={location.coordinates.y}
                      r="8"
                      className="animate-pulse active-location"
                      opacity="0.1"
                    />
                    <circle
                      cx={location.coordinates.x}
                      cy={location.coordinates.y}
                      r="5"
                      stroke="white"
                      strokeWidth="2"
                      className="cursor-pointer transition-all duration-300 active-location location-dot"
                      onClick={() => handleLocationClick(location.id)}
                      onMouseEnter={() => handleLocationHover(location.id)}
                      onMouseLeave={() => handleLocationHover(null)}
                    />
                    {/* <circle
                      cx={location.coordinates.x}
                      cy={location.coordinates.y}
                      r="2"
                      fill="white"
                      className="pointer-events-none"
                    /> */}
            {/* <circle
              cx={location.coordinates.x}
              cy={location.coordinates.y}
              r="4"
              className={`location-dot active-location ${
                selectedLocation === location.id ? 'ring-2 ring-white ring-offset-2' : ''
              }`}
            /> */}
          </g>
        ))}

        {/* Potential Locations */}
        {potentialLocations.map((location) => (
          <g key={location.id} className="location-group">
            <circle
              cx={location.coordinates.x}
              cy={location.coordinates.y}
              r="5"
              className="location-dot potential-location"
              stroke="white"
              strokeWidth="2"
              onClick={() => handleLocationClick(location.id)}
              onMouseEnter={() => handleLocationHover(location.id)}
              onMouseLeave={() => handleLocationHover(null)}
            />
            <g className='location-text'>
              {(() => {
                const fontSize = 8;
                const paddingX = 8;
                const textLength = location.name.length;
                const estTextWidth = textLength * fontSize * 0.6;
                const rectWidth = estTextWidth + paddingX * 2;
                const rectX = location.coordinates.x - rectWidth / 2;
                return (
                  <>
                    <rect
                      x={rectX}
                      y={location.coordinates.y - 27}
                      width={rectWidth}
                      height="16"
                      rx="4"
                      fill="white"
                      opacity="0.85"
                    />
                    <text
                      x={location.coordinates.x}
                      y={location.coordinates.y - 18}
                      textAnchor="middle"
                      className="text-xs fill-slate-800"
                      style={{ fontSize: `${fontSize}px`, dominantBaseline: "middle" }}
                    >
                      {location.name}
                    </text>
                  </>
                );
              })()}
            </g>
          </g>
        ))}

        {/* Interactive overlays for specific areas */}
        <g 
          id="munich-overlay"
          className="cursor-pointer"
          onMouseEnter={() => handleLocationHover('munich')}
          onMouseLeave={() => handleLocationHover(null)}
          onClick={() => handleLocationClick('munich')}
        >
          <rect
            x="105.796"
            y="161.389"
            width="3.707"
            height="2.153"
            fill="transparent"
            className="hover:fill-secondary/10"
          />
        </g>

        <g 
          id="mallorca-overlay"
          className="cursor-pointer"
          onMouseEnter={() => handleLocationHover('mallorca')}
          onMouseLeave={() => handleLocationHover(null)}
          onClick={() => handleLocationClick('mallorca')}
        >
          <rect
            x="86"
            y="202"
            width="3.707"
            height="2.153"
            fill="transparent"
            className="hover:fill-secondary/10"
          />
        </g>
      </svg>

      {/* Legend */}
      <div className="absolute bottom-2 left-4 flex items-center gap-4 text-xs text-muted-foreground">
        <div className="flex items-center gap-2">
          <div 
            className="w-3 h-3 rounded-full"
            style={{ backgroundColor: activeLocationColor }}
          ></div>
          <span>{t('map.active')}</span>
        </div>
        {showPotentialLocations && (
          <div className="flex items-center gap-2">
            <div 
              className="w-3 h-3 rounded-full"
              style={{ backgroundColor: potentialLocationColor }}
            ></div>
            <span>{t('map.planned')}</span>
          </div>
        )}
      </div>
    </div>
  );
}
